import Foundation

/// This enum describes the horizontal direction of the animation
/// case: leftToRight - The animation starts on the left and continues to the right
/// case: rightToLeft - The animation starts on the right and continues to the left
public enum AnimationDirection: Int {
    case leftToRight
    case rightToLeft
}
